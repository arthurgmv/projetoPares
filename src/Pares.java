public class Pares {
    public static void main(String[] args) {
        for (int i = 0; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }
            ;
        }
        ;
        String letters = "Hello World";
        for (int i = 0; i <= letters.length() - 1; i++){
            System.out.println(letters.charAt(i) + " ");
        }
    }
}
